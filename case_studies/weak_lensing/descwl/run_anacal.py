def main():
    # Determine config file path
    if len(sys.argv) > 1:
        config_file = sys.argv[1]
    else:
        # Default to config_anacal.yaml in same directory as script
        script_dir = os.path.dirname(os.path.abspath(__file__))
        config_file = os.path.join(script_dir, "config_anacal.yaml")

    if not os.path.exists(config_file):
        print(f"Error: Config file not found: {config_file}")
        print("Usage: python run_anacal.py config_anacal.yaml")
        sys.exit(1)

    start_datetime = datetime.now()
    print("=== ANACAL PROCESSING ===")
    print(f"Start time: {start_datetime.strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 50)

    # Load config from YAML
    print(f"Loading config from: {config_file}")
    with open(config_file) as f:
        user_config = yaml.safe_load(f)

    # Merge with defaults for AnaCal parameters
    config = DEFAULT_CONFIG.copy()
    config.update(user_config)

    # Extract required paths
    input_dir = config.get("input_dir")
    output_file = config.get("output_file")

    if not input_dir or not output_file:
        print("Error: config must specify 'input_dir' and 'output_file'")
        sys.exit(1)

    # Extract optional settings with defaults
    start_idx = config.get("start_idx", 0)
    end_idx = config.get("end_idx")  # None = all files
    checkpoint_interval = config.get("checkpoint_interval", 50)
    n_workers = config.get("n_workers", 1)
    verbose = config.get("verbose", False)

    print("Configuration:")
    for key, value in config.items():
        print(f"  {key}: {value}")

    # Ensure output directory exists
    output_dir = os.path.dirname(output_file)
    if output_dir and not os.path.exists(output_dir):
        os.makedirs(output_dir)

    # Run processing
    process_all_images(
        input_dir=input_dir,
        output_file=output_file,
        config=config,
        start_idx=start_idx,
        end_idx=end_idx,
        checkpoint_interval=checkpoint_interval,
        n_workers=n_workers,
        verbose=verbose,
    )

    print(f"\nEnd time: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 50)


if __name__ == "__main__":
    main()
