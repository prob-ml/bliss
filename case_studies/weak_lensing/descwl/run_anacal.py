def process_single_file(file_path, config):
    """
    Process a single image file with AnaCal.

    Parameters
    ----------
    file_path : str
        Path to the .pt file
    config : dict
        AnaCal configuration

    Returns
    -------
    dict with results or None if processing failed
    """
    try:
        data = load_image_data(file_path)
        images = data["images"]
        catalog = data["tile_catalog"]
        anacal_data = data["anacal_data"]

        n_bands = images.shape[0]

        # Get AnaCal inputs
        psf_image = anacal_data["psf_image"]
        masks_dict = anacal_data["masks"]
        band_variances = anacal_data["variances"]
        bright_star_catalog = anacal_data.get("bright_star_catalog")
        pixel_scale = anacal_data.get("pixel_scale", config["pixel_scale"])

        # Process based on band count
        if n_bands == 1:
            # Single-band processing
            band = next(iter(masks_dict.keys()))
            single_band_mask = masks_dict.get(band)
            single_band_variance = band_variances.get(band, 0.354)

            e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections = anacal_single_image(
                images,
                psf_image,
                mask_array=single_band_mask,
                star_catalog=bright_star_catalog,
                npix=config["npix"],
                sigma_arcsec=config["sigma_arcsec"],
                mag_zero=config["mag_zero"],
                pixel_scale=pixel_scale,
                noise_variance=single_band_variance,
            )
        else:
            # Multi-band processing
            e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections, _ = anacal_multiband_combined(
                images,
                psf_image,
                masks_dict=masks_dict,
                combine_method=config["combine_method"],
                mask_combine_method=config["mask_combine_method"],
                star_catalog=bright_star_catalog,
                npix=config["npix"],
                sigma_arcsec=config["sigma_arcsec"],
                mag_zero=config["mag_zero"],
                pixel_scale=pixel_scale,
                band_variances=band_variances,
            )

        return {
            "e1_sum": float(e1_sum),
            "e1g1_sum": float(e1g1_sum),
            "e2_sum": float(e2_sum),
            "e2g2_sum": float(e2g2_sum),
            "num_detections": int(num_detections),
            "shear_1": float(catalog["shear_1"]),
            "shear_2": float(catalog["shear_2"]),
            "index": extract_index_from_filename(file_path),
        }

    except Exception as e:
        print(f"Error processing {file_path}: {e}")
        return None


def process_single_file_worker(args):
    """Worker function for multiprocessing - unpacks args tuple."""
    file_path, config = args
    return process_single_file(file_path, config)


def save_checkpoint(results, output_file, is_final=False):
    """Save current results to a checkpoint file."""
    if is_final:
        checkpoint_path = output_file
    else:
        # Create checkpoint path by adding .checkpoint before .pt
        base, ext = os.path.splitext(output_file)
        checkpoint_path = f"{base}_checkpoint{ext}"

    # Convert lists to numpy arrays
    output = {
        "e1_sum": np.array(results["e1_sum"]),
        "e1g1_sum": np.array(results["e1g1_sum"]),
        "e2_sum": np.array(results["e2_sum"]),
        "e2g2_sum": np.array(results["e2g2_sum"]),
        "num_detections": np.array(results["num_detections"]),
        "shear_1": np.array(results["shear_1"]),
        "shear_2": np.array(results["shear_2"]),
        "indices": np.array(results["indices"]),
        # Aggregated statistics
        "total_e1_sum": float(np.sum(results["e1_sum"])),
        "total_e1g1_sum": float(np.sum(results["e1g1_sum"])),
        "total_e2_sum": float(np.sum(results["e2_sum"])),
        "total_e2g2_sum": float(np.sum(results["e2g2_sum"])),
        "total_detections": int(np.sum(results["num_detections"])),
        # Metadata
        "n_images_processed": len(results["e1_sum"]),
        "config": results.get("config", {}),
        "timestamp": datetime.now().isoformat(),
    }

    torch.save(output, checkpoint_path)
    return checkpoint_path


def process_all_images(
    input_dir,
    output_file,
    config,
    start_idx=0,
    end_idx=None,
    checkpoint_interval=50,
    n_workers=1,
    verbose=False,
):
    """
    Process all images in the input directory.

    Parameters
    ----------
    input_dir : str
        Directory containing dataset_*.pt files
    output_file : str
        Path for output results file
    config : dict
        AnaCal configuration
    start_idx : int
        Start processing from this index
    end_idx : int or None
        End processing at this index (None = all files)
    checkpoint_interval : int
        Save checkpoint every N images
    n_workers : int
        Number of parallel workers (1 = sequential)
    verbose : bool
        Print detailed progress

    Returns
    -------
    dict with accumulated results
    """
    # Find all image files
    files = find_image_files(input_dir)
    print(f"Found {len(files)} image files in {input_dir}")

    if len(files) == 0:
        print("No files to process!")
        return None

    # Apply start/end indices
    files = files[start_idx:end_idx] if end_idx is not None else files[start_idx:]

    print(f"Processing {len(files)} files (indices {start_idx} to {start_idx + len(files) - 1})")
    print(f"Workers: {n_workers}")

    # Initialize result accumulators
    results = {
        "e1_sum": [],
        "e1g1_sum": [],
        "e2_sum": [],
        "e2g2_sum": [],
        "num_detections": [],
        "shear_1": [],
        "shear_2": [],
        "indices": [],
        "config": config,
    }

    start_time = time.time()
    failed_count = 0
    processed_count = 0

    def append_result(result):
        """Helper to append a single result to accumulator."""
        results["e1_sum"].append(result["e1_sum"])
        results["e1g1_sum"].append(result["e1g1_sum"])
        results["e2_sum"].append(result["e2_sum"])
        results["e2g2_sum"].append(result["e2g2_sum"])
        results["num_detections"].append(result["num_detections"])
        results["shear_1"].append(result["shear_1"])
        results["shear_2"].append(result["shear_2"])
        results["indices"].append(result["index"])

    if n_workers <= 1:
        # Sequential processing
        for i, file_path in enumerate(tqdm(files, desc="Processing", unit="img")):
            result = process_single_file(file_path, config)

            if result is not None:
                append_result(result)
                processed_count += 1
            else:
                failed_count += 1

            # Save checkpoint periodically
            if checkpoint_interval > 0 and (i + 1) % checkpoint_interval == 0:
                checkpoint_path = save_checkpoint(results, output_file, is_final=False)
                if verbose:
                    print(f"\nCheckpoint saved after {i + 1} images: {checkpoint_path}")
    else:
        # Parallel processing with multiprocessing
        args_list = [(f, config) for f in files]

        with ProcessPoolExecutor(max_workers=n_workers) as executor:
            # Submit all tasks
            future_to_file = {
                executor.submit(process_single_file_worker, args): args[0] for args in args_list
            }

            # Process results as they complete
            for future in tqdm(
                as_completed(future_to_file), total=len(files), desc="Processing", unit="img"
            ):
                file_path = future_to_file[future]
                try:
                    result = future.result()
                    if result is not None:
                        append_result(result)
                        processed_count += 1
                    else:
                        failed_count += 1
                except Exception as e:
                    print(f"\nError processing {file_path}: {e}")
                    failed_count += 1

                # Save checkpoint periodically
                if checkpoint_interval > 0 and processed_count % checkpoint_interval == 0:
                    checkpoint_path = save_checkpoint(results, output_file, is_final=False)
                    if verbose:
                        print(
                            f"\nCheckpoint saved after {processed_count} images: {checkpoint_path}"
                        )

    # Save final results
    elapsed_time = time.time() - start_time
    results["processing_time"] = elapsed_time

    final_path = save_checkpoint(results, output_file, is_final=True)
    print(f"\nFinal results saved to: {final_path}")

    # Remove checkpoint file if it exists
    base, ext = os.path.splitext(output_file)
    checkpoint_path = f"{base}_checkpoint{ext}"
    if os.path.exists(checkpoint_path):
        os.remove(checkpoint_path)
        print(f"Removed checkpoint file: {checkpoint_path}")

    # Print summary
    n_processed = len(results["e1_sum"])
    print("\n=== PROCESSING COMPLETE ===")
    print(f"Successfully processed: {n_processed}/{len(files)} images")
    if failed_count > 0:
        print(f"Failed: {failed_count} files")
    print(f"Total time: {elapsed_time:.2f} seconds")
    print(f"Average time per image: {elapsed_time / max(n_processed, 1):.3f} seconds")
    if n_workers > 1:
        print(f"Effective throughput: {n_processed / elapsed_time:.2f} img/sec")
    print(f"Total detections: {np.sum(results['num_detections'])}")

    return results


