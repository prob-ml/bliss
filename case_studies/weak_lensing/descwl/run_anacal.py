def find_image_files(input_dir, pattern="dataset_*_size_1.pt"):
    """Find all image files in the input directory."""
    files = sorted(glob.glob(os.path.join(input_dir, pattern)))
    return files


def extract_index_from_filename(filename):
    """Extract the image index from filename like 'dataset_123_size_1.pt'."""
    basename = os.path.basename(filename)
    # Format: dataset_{idx}_size_1.pt
    parts = basename.replace(".pt", "").split("_")
    try:
        return int(parts[1])
    except (IndexError, ValueError):
        return -1


def load_image_data(file_path):
    """Load image data from a .pt file."""
    data_list = torch.load(file_path, weights_only=False)
    return data_list[0]  # First element of the list


