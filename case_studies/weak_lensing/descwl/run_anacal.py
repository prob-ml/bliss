def anacal_single_image(
    image,
    psf_input,
    mask_array=None,
    star_catalog=None,
    npix=64,
    sigma_arcsec=0.52,
    mag_zero=30.0,
    pixel_scale=0.2,
    noise_variance=0.354,
):
    """
    Process a single-band image with anacal.

    Parameters
    ----------
    image : torch.Tensor
        Image tensor [1, H, W] or [H, W]
    psf_input : np.ndarray or GalSimPsfWrapper
        PSF image or wrapper object
    mask_array : np.ndarray or None
        Pixel mask for the band
    star_catalog : np.ndarray or None
        Bright star catalog for masking
    npix : int
        PSF cutout size
    sigma_arcsec : float
        Gaussian smoothing sigma
    mag_zero : float
        Zero point magnitude
    pixel_scale : float
        Arcsec per pixel
    noise_variance : float
        Noise variance estimate

    Returns
    -------
    e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections
    """
    fpfs_config = anacal.fpfs.FpfsConfig(
        npix=npix,
        sigma_arcsec=sigma_arcsec,
    )

    # Handle different input shapes
    if isinstance(image, torch.Tensor):
        if len(image.shape) == 3:
            if image.shape[0] == 1:
                gal_array = image[0].numpy()
            else:
                raise ValueError(f"Expected single-band image, got {image.shape[0]} bands")
        elif len(image.shape) == 2:
            gal_array = image.numpy()
        else:
            raise ValueError(f"Unexpected image shape: {image.shape}")
    else:
        gal_array = np.asarray(image)

    noise_array = np.random.normal(0, np.sqrt(noise_variance), gal_array.shape).astype(np.float64)

    # Handle PSF
    if isinstance(psf_input, np.ndarray):
        out = anacal.fpfs.process_image(
            fpfs_config=fpfs_config,
            mag_zero=mag_zero,
            gal_array=gal_array,
            psf_array=psf_input,
            pixel_scale=pixel_scale,
            noise_variance=noise_variance,
            noise_array=noise_array,
            mask_array=mask_array,
            star_catalog=star_catalog,
            detection=None,
        )
    else:
        center_psf = psf_input.draw(gal_array.shape[1] // 2, gal_array.shape[0] // 2)
        out = anacal.fpfs.process_image(
            fpfs_config=fpfs_config,
            mag_zero=mag_zero,
            gal_array=gal_array,
            psf_array=center_psf,
            psf_object=psf_input,
            pixel_scale=pixel_scale,
            noise_variance=noise_variance,
            noise_array=noise_array,
            mask_array=mask_array,
            star_catalog=star_catalog,
            detection=None,
        )

    # Extract results
    e1 = out["fpfs_w"] * out["fpfs_e1"]
    e1g1 = out["fpfs_dw_dg1"] * out["fpfs_e1"] + out["fpfs_w"] * out["fpfs_de1_dg1"]
    e1_sum = np.sum(e1)
    e1g1_sum = np.sum(e1g1)

    e2 = out["fpfs_w"] * out["fpfs_e2"]
    e2g2 = out["fpfs_dw_dg2"] * out["fpfs_e2"] + out["fpfs_w"] * out["fpfs_de2_dg2"]
    e2_sum = np.sum(e2)
    e2g2_sum = np.sum(e2g2)

    num_detections = len(e1)

    return e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections


def anacal_multiband_combined(
    images_tensor,
    psf_input,
    masks_dict=None,
    combine_method="inverse_variance",
    mask_combine_method="union",
    star_catalog=None,
    npix=64,
    sigma_arcsec=0.52,
    mag_zero=30.0,
    pixel_scale=0.2,
    band_variances=None,
):
    """
    Process multi-band images by combining them first, then running anacal.

    Parameters
    ----------
    images_tensor : torch.Tensor
        Shape: [n_bands, height, width]
    psf_input : np.ndarray or GalSimPsfWrapper
        PSF image or wrapper
    masks_dict : dict or None
        Per-band mask arrays
    combine_method : str
        Method for combining bands
    mask_combine_method : str
        Method for combining masks
    star_catalog : np.ndarray or None
        Bright star catalog for masking
    npix : int
        PSF cutout size
    sigma_arcsec : float
        Gaussian smoothing sigma
    mag_zero : float
        Zero point magnitude
    pixel_scale : float
        Arcsec per pixel
    band_variances : dict or None
        Per-band variance estimates

    Returns
    -------
    e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections
    """
    # Combine images with proper inverse-variance weighting
    combined_image, combined_variance = combine_multiband_images(
        images_tensor, band_variances=band_variances, method=combine_method
    )
    gal_array = combined_image.numpy()

    # Combine masks
    combined_mask = None
    if masks_dict is not None:
        combined_mask = combine_multiband_masks(masks_dict, method=mask_combine_method)

    # Generate noise array with COMBINED variance
    noise_array = np.random.normal(0, np.sqrt(combined_variance), gal_array.shape).astype(
        np.float64
    )

    fpfs_config = anacal.fpfs.FpfsConfig(
        npix=npix,
        sigma_arcsec=sigma_arcsec,
    )

    if isinstance(psf_input, np.ndarray):
        out = anacal.fpfs.process_image(
            fpfs_config=fpfs_config,
            mag_zero=mag_zero,
            gal_array=gal_array,
            psf_array=psf_input,
            pixel_scale=pixel_scale,
            noise_variance=combined_variance,
            noise_array=noise_array,
            mask_array=combined_mask,
            star_catalog=star_catalog,
            detection=None,
        )
    else:
        center_psf = psf_input.draw(gal_array.shape[1] // 2, gal_array.shape[0] // 2)
        out = anacal.fpfs.process_image(
            fpfs_config=fpfs_config,
            mag_zero=mag_zero,
            gal_array=gal_array,
            psf_array=center_psf,
            psf_object=psf_input,
            pixel_scale=pixel_scale,
            noise_variance=combined_variance,
            noise_array=noise_array,
            mask_array=combined_mask,
            star_catalog=star_catalog,
            detection=None,
        )

    # Extract results
    e1 = out["fpfs_w"] * out["fpfs_e1"]
    e1g1 = out["fpfs_dw_dg1"] * out["fpfs_e1"] + out["fpfs_w"] * out["fpfs_de1_dg1"]
    e1_sum = np.sum(e1)
    e1g1_sum = np.sum(e1g1)

    e2 = out["fpfs_w"] * out["fpfs_e2"]
    e2g2 = out["fpfs_dw_dg2"] * out["fpfs_e2"] + out["fpfs_w"] * out["fpfs_de2_dg2"]
    e2_sum = np.sum(e2)
    e2g2_sum = np.sum(e2g2)

    num_detections = len(e1)

    return e1_sum, e1g1_sum, e2_sum, e2g2_sum, num_detections, out


